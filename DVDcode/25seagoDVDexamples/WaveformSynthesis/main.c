int main(int argc, char* argv[])
{
    IBNibRef 		nibRef;
    WindowRef 		window;
    
    OSStatus		err;
	EventTypeSpec 	commSpec = { kEventClassCommand, kEventProcessCommand };

	
	double			fundamental_freq;
	CFStringRef		fundamental_freq_CFString ;
	
    // Create a Nib reference passing the name of the nib file (without the .nib extension)
    // CreateNibReference only searches into the application bundle.
    err = CreateNibReference(CFSTR("main"), &nibRef);
    require_noerr( err, CantGetNibRef );
    
    // Once the nib reference is created, set the menu bar. "MainMenu" is the name of the menu bar
    // object. This name is set in InterfaceBuilder when the nib is created.
    err = SetMenuBarFromNib(nibRef, CFSTR("MenuBar"));
    require_noerr( err, CantSetMenuBar );
    
    // Then create a window. "MainWindow" is the name of the window object. This name is set in 
    // InterfaceBuilder when the nib is created.
    err = CreateWindowFromNib(nibRef, CFSTR("MainWindow"), &window);
    require_noerr( err, CantCreateWindow );

    // We don't need the nib reference anymore.
    DisposeNibReference(nibRef);
    
    // Generate the spectrum
    
    the_spectrum.setSpectrum(0.05,0.05,0.05,0.05,0.05,0.05,
						     0.05,0.05,0.05,0.05,0.05,0.05,
                             0.05,0.05,0.05,0.05,0.05,0.05,
							 0.05,0.05);
    
	GetControlByID (window, &FreqControlID, &FreqControlHandle);
	GetControlByID (window, &FreqDisplayControlID, &FreqDisplayControlHandle);
	
	fundamental_freq = GetControl32BitValue( FreqControlHandle );
	fundamental_freq_CFString = CFStringCreateWithFormat( NULL, NULL, CFSTR("%3.0f"), fundamental_freq );
	
	SetControlData( FreqDisplayControlHandle, 0, kControlEditTextCFStringTag, sizeof(CFStringRef), &fundamental_freq_CFString );					
	DrawOneControl( FreqDisplayControlHandle );		
      
    // The window was created hidden so show it.
    ShowWindow( window );
    
    //
    InstallWindowEventHandler( window, NewEventHandlerUPP(MainWindowCommandHandler), 1, &commSpec, (void *) window, NULL );
    // Call the event loop
    RunApplicationEventLoop();

CantCreateWindow:
CantSetMenuBar:
CantGetNibRef:
	return err;
}

