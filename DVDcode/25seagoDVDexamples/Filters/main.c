filter						left_channel_filter, right_channel_filter;        // we need two filters for each channel...

enum						FILTER {LOW_PASS, HIGH_PASS, BAND_PASS, NOTCH};   // type of filter
int							FilterType = LOW_PASS;                            // low pass by default
int							Fourier_component = 1;                            
Boolean						InputFileOpen=FALSE, OutputFileOpen=FALSE;        // flags

// control IDs and handles for the 'filter' button and the pop up menus - let's make these global....

